/*
 Question:
 Write a Java program to print numbers from 10 to 1
 using a for loop.
*/

public class PrintTenToOne {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 10 to 1
        for (int i = 10; i >= 1; i = i - 1) {
            System.out.println(i);
        }
    }
}
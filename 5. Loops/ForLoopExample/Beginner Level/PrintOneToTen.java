/*
 Question:
 Write a Java program to print numbers from 1 to 10
 using a for loop.
*/

public class PrintOneToTen {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 1 to 10
        for (int i = 1; i <= 10; i = i + 1) {
            System.out.println(i);
        }
    }
}
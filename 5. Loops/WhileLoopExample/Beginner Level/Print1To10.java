/*
 Question:
 Write a Java program to print numbers from 1 to 10
 using a while loop.
*/

public class Print1To10 {
    public static void main(String[] args) {
        int n = 1; // Initialize counter variable
        while (n<=10){
            System.out.println(n);   // Print current value of counter variable
            n++; // Increment counter variable
        }
   }
}